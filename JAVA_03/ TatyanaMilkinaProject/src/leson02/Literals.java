
public class Literals {
    public static void main(String[] args) {
        System.out.println("Лгмческие литералы" + true);
        System.out.println("Целочисленный 10-й литералы - " + 5);
        System.out.println("Целочисленный 8-й литералы - " + 011);
        System.out.println("Целочисленный 16-й литералы - " + 0Xab11);
        System.out.println("Целочисленный двоичный литералы - " + 0b11);
        System.out.println("Дробный двоичный литералы - " + 4.5);
        System.out.println("Символьный двоичный литералы - " + 'r');

    }
}
